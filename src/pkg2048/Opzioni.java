            File file = new File("option.gio");
            if(file.exists())
                file.delete();
            file.createNewFile();
            
            FileWriter fw = new FileWriter(file);
            fw.write(jTextField1.getText()+"\n"+jTextField2.getText());
            fw.flush();
            fw.close();
